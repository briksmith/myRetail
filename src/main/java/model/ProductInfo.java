package model;

public class ProductInfo
{
	private int id;
	private String name;
	private double price;
	private String currencyCode;
	
	public ProductInfo() 
	{}
	
	public int getId()
	{
		return id;
	}

	public String getName()
	{
		return name;
	}

	public double getPrice()
	{
		return price;
	}

	public String getCurrencyCode()
	{
		return currencyCode;
	}
}
